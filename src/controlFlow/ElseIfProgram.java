package controlFlow;

public class ElseIfProgram{
public static void main (String[]args) {

	
		

		String hobby = "dancing";
		if(hobby.equalsIgnoreCase("dancing")){
			System.out.println("My hobby is dancing");
		}else if (hobby.equalsIgnoreCase("reading")){
			System.out.println("My hobby is reading");
			}else if (hobby.equalsIgnoreCase("cooking")){
				System.out.println("My hobby is cooking");
				}else {
					System.out.println("hobby  may be somethingelse ");
						}

			}
	}